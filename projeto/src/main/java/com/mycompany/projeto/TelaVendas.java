    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        String qnt = txtQnt.getText();
        String cboProduto = "";
        
        if(txtQnt.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite a quantidade do produto.");
            return;
        }
        
        if(cboProdutos.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(this, "Escolha um produto antes de efutar a compra.");
        } else{
            cboProduto = cboProdutos.getSelectedItem().toString();
        }
         
        JOptionPane.showMessageDialog(this, "VocÃª comprou: " + cboProdutos);
        
    }//GEN-LAST:event_btnComprarActionPerformed

