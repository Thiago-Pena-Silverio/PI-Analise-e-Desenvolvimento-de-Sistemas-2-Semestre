    private void btnConfirmarCdstrActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmarCdstrActionPerformed
        String nome = txtNome.getText();
        String cpf = txtCpf.getText();
        String email = txtEmail.getText();
        String end = txtEnd.getText();
        
        if(txtNome.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite o seu nome.");
            return;
        }
        
        if(txtCpf.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite o seu CPF.");
            return;
        }
        
        if(txtEmail.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite o seu Email.");
            return;
        }
        
        if(txtEnd.getText().trim().equals("")){
            JOptionPane.showMessageDialog(this, "Digite o seu Endere√ßo.");
            return;
        }
        
        JOptionPane.showMessageDialog(this, "Seu nome √©: " + nome 
                                    + "\n Seu CPF √©: " + cpf
                                    + "\n Seu Email √©: " + email
                                    + "\n Seu Endere√ßo √©: " + end);
    }//GEN-LAST:event_btnConfirmarCdstrActionPerformed

